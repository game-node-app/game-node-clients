import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const sourcePath = path.join(
  __dirname,
  "..",
  "src",
  "translations",
  "json",
  "en.json",
);
const outputPath = path.join(
  __dirname,
  "..",
  "src",
  "generated",
  "translation-schema.d.ts",
);

function toTypeLiteral(value, indentLevel = 0) {
  const indent = "  ".repeat(indentLevel);
  const nextIndent = "  ".repeat(indentLevel + 1);

  if (Array.isArray(value)) {
    if (value.length === 0) return "readonly unknown[]";
    const itemType = toTypeLiteral(value[0], indentLevel + 1);
    return `readonly (${itemType})[]`;
  }

  if (value === null) return "null";

  if (typeof value === "object") {
    const entries = Object.entries(value);
    if (entries.length === 0) return "{}";

    const lines = entries.map(([key, entryValue]) => {
      const keyToken = /^[$A-Z_][0-9A-Z_$]*$/i.test(key)
        ? key
        : JSON.stringify(key);
      return `${nextIndent}${keyToken}: ${toTypeLiteral(entryValue, indentLevel + 1)};`;
    });

    return `{
${lines.join("\n")}
${indent}}`;
  }

  if (typeof value === "string") return "string";
  if (typeof value === "number") return "number";
  if (typeof value === "boolean") return "boolean";

  return "unknown";
}

function main() {
  const raw = fs.readFileSync(sourcePath, "utf8");
  let data;

  try {
    data = JSON.parse(raw);
  } catch (error) {
    if (error instanceof SyntaxError) {
      console.error(`Invalid JSON in ${sourcePath}`);
      console.error(error.message);
      process.exit(1);
    }

    throw error;
  }

  const output = `/* eslint-disable */\n// Auto-generated by packages/locales/scripts/generate-i18n-types.mjs\n// Do not edit manually.\n\nexport type TranslationSchema = ${toTypeLiteral(data)};\n`;

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, output, "utf8");

  console.log(`Generated ${path.relative(process.cwd(), outputPath)}`);
}

main();
